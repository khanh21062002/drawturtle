import turtle

#đặt kích thước viền cho hình tròn là 5
turtle.pensize (5)
 
#đặt màu sắc cho viền hình tròn là màu đỏ
turtle.pencolor ("red")

#đặt màu nền cho hình tròn là màu xanh
turtle.fillcolor ("blue")
turtle.begin_fill()

#đặt bán kính của hình tròn là 100
turtle.circle (100)
turtle.end_fill()
turtle.done